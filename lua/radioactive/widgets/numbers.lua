local M = {}

function M.get_ascii_lines(number)
	local str = tostring(number)
	local lines = {}
	local height = 25
	for i = 1, #str do
		local line = ""
		local split = vim.split(numbers[string.sub(str, i, i)], "\n")
		for j = 1, height, 1 do
			line = line .. split[j]
		end
		table.insert(lines, line)
	end
	return lines
end

return M
